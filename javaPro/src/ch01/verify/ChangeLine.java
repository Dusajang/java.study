package ch01.verify;

public class ChangeLine {

	public static void main(String[] args) {
		System.out.println("송세호");
		System.out.print("송세호" + '\n');
		System.out.print("송세호" + '\r');
		System.out.print("송세호\n");
		System.out.print("송세호\r");
		
		System.out.print("송세호");
		System.out.println("");
		System.out.print("송세호");
		
		
		System.out.println(3 + 5 + '0');
		System.out.println(3 + 5 + "0" );

	}

}
