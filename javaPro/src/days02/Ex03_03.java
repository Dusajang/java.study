		System.out.println("송세호");
		System.out.println("이시훈");
		System.out.println("이창익");
		
		//println() 매서드 - 출력
		System.out.print("송세호"+'\n');
		System.out.print("송세호\n");
		System.out.print("이시훈"+'\r');
		
		System.out.print("이창익");
		System.out.println("");
		System.out.print("이창익");
		
		//줄바꿈 여부(개행) 여부   ==   CR '\r'  +   LF '\n'

